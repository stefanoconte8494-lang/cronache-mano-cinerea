export const npcsByFirstAppearance = [...npcs].sort((a, b) => {
  const firstA = Number.isFinite(a.firstSession) ? a.firstSession : Number.MAX_SAFE_INTEGER
  const firstB = Number.isFinite(b.firstSession) ? b.firstSession : Number.MAX_SAFE_INTEGER

  if (firstA !== firstB) {
    return firstA - firstB
  }

  return a.name.localeCompare(b.name, 'it')
})
